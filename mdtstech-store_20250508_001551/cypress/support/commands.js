// Custom command to check for accessibility issues
import 'cypress-axe';

// Custom command to login
Cypress.Commands.add('login', (email, password) => {
  cy.visit('/auth/signin');
  cy.get('input[name="email"]').type(email);
  cy.get('input[name="password"]').type(password);
  cy.get('button[type="submit"]').click();
  cy.url().should('not.include', '/auth/signin');
});

// Custom command to add a product to cart
Cypress.Commands.add('addToCart', (productUrl) => {
  cy.visit(productUrl);
  cy.get('button').contains('Add to Cart').click();
  cy.get('[data-testid="cart-notification"]').should('be.visible');
});

// Custom command to check if element is in viewport
Cypress.Commands.add('isInViewport', { prevSubject: true }, (subject) => {
  const bottom = Cypress.$(cy.state('window')).height();
  const rect = subject[0].getBoundingClientRect();
  
  expect(rect.top).to.be.lessThan(bottom);
  expect(rect.bottom).to.be.greaterThan(0);
  
  return subject;
});
